import timezone_mock from 'timezone-mock';
import test from '../testFunctions';

describe('isDate', () => {
  it('should validate date', () => {
    test({
      validator: 'isDate',
      valid: [
        new Date(),
        new Date([2014, 2, 15]),
        new Date('2014-03-15'),
        '2020/02/29',
        '2020-02-19',
      ],
      invalid: [
        '',
        '15072002',
        null,
        undefined,
        { year: 2002, month: 7, day: 15 },
        42,
        { toString() { return '[object Date]'; } }, // faking
        '2020-02-30', // invalid date
        '2019-02-29', // non-leap year
        '2020-04-31', // invalid date
        '2020/03-15', // mixed delimiter
        '-2020-04-19',
        '-2023/05/24',
        'abc-2023/05/24',
        '2024',
        '2024-',
        '2024-05',
        '2024-05-',
        '2024-05-01-',
        '2024-05-01-abc',
        '2024/',
        '2024/05',
        '2024/05/',
        '2024/05/01/',
        '2024/05/01/abc',
      ],
    });
    test({
      validator: 'isDate',
      args: ['DD/MM/YYYY'], // old format for backward compatibility
      valid: [
        '15-07-2002',
        '15/07/2002',
      ],
      invalid: [
        '15/7/2002',
        '15-7-2002',
        '15/7/02',
        '15-7-02',
        '15-07/2002',
        '2024',
        '2024-',
        '2024-05',
        '2024-05-',
        '2024-05-01-',
        '2024-05-01-abc',
        '2024/',
        '2024/05',
        '2024/05/',
        '2024/05/01/',
        '2024/05/01/abc',
        '15/',
        '15/07',
        '15/07/',
        '15/07/2024/',
      ],
    });
    test({
      validator: 'isDate',
      args: [{ format: 'DD/MM/YYYY' }],
      valid: [
        '15-07-2002',
        '15/07/2002',
      ],
      invalid: [
        '15/7/2002',
        '15-7-2002',
        '15/7/02',
        '15-7-02',
        '15-07/2002',
        '2024',
        '2024-',
        '2024-05',
        '2024-05-',
        '2024-05-01-',
        '2024-05-01-abc',
        '2024/',
        '2024/05',
        '2024/05/',
        '2024/05/01/',
        '2024/05/01/abc',
        '15/',
        '15/07',
        '15/07/',
        '15/07/2024/',
      ],
    });
    test({
      validator: 'isDate',
      args: [{ format: 'DD/MM/YY' }],
      valid: [
        '15-07-02',
        '15/07/02',
      ],
      invalid: [
        '15/7/2002',
        '15-7-2002',
        '15/07-02',
        '30/04/--',
        '2024',
        '2024-',
        '2024-05',
        '2024-05-',
        '2024-05-01-',
        '2024-05-01-abc',
        '2024/',
        '2024/05',
        '2024/05/',
        '2024/05/01/',
        '2024/05/01/abc',
        '15/',
        '15/07',
        '15/07/',
        '15/07/2024/',
        '15/07/24/',
      ],
    });
    test({
      validator: 'isDate',
      args: [{ format: 'D/M/YY' }],
      valid: [
        '5-7-02',
        '5/7/02',
      ],
      invalid: [
        '5/07/02',
        '15/7/02',
        '15-7-02',
        '5/7-02',
        '3/4/aa',
        '2024/',
        '2024/05',
        '2024/05/',
        '2024/05/01/',
        '2024/05/01/abc',
        '15/',
        '15/07',
        '15/07/',
        '15/07/2024/',
        '15/07/24/',
      ],
    });
    test({
      validator: 'isDate',
      args: [{ format: 'DD/MM/YYYY', strictMode: true }],
      valid: [
        '15/07/2002',
      ],
      invalid: [
        '15-07-2002',
        '15/7/2002',
        '15-7-2002',
        '15/7/02',
        '15-7-02',
        '15-07/2002',
        '2024/',
        '2024/05',
        '2024/05/',
        '2024/05/01/',
        '2024/05/01/abc',
        '15/',
        '15/07',
        '15/07/',
        '15/07/2024/',
        '15/07/24/',
      ],
    });
    test({
      validator: 'isDate',
      args: [{ strictMode: true }],
      valid: [
        '2020/01/15',
        '2014/02/15',
        '2014/03/15',
        '2020/02/29',
      ],
      invalid: [
        '2014-02-15',
        '2020-02-29',
        '15-07/2002',
        new Date(),
        new Date([2014, 2, 15]),
        new Date('2014-03-15'),
        '-2020-04-19',
        '-2023/05/24',
        'abc-2023/05/24',
        '2024',
        '2024-',
        '2024-05',
        '2024-05-',
        '2024-05-01-',
        '2024-05-01-abc',
        '2024/',
        '2024/05',
        '2024/05/',
        '2024/05/01/',
        '2024/05/01/abc',
      ],
    });
    test({
      validator: 'isDate',
      args: [{ delimiters: ['/', ' '] }],
      valid: [
        new Date(),
        new Date([2014, 2, 15]),
        new Date('2014-03-15'),
        '2020/02/29',
        '2020 02 29',
      ],
      invalid: [
        '2020-02-29',
        '',
        '15072002',
        null,
        undefined,
        { year: 2002, month: 7, day: 15 },
        42,
        { toString() { return '[object Date]'; } },
        '2020/02/30',
        '2019/02/29',
        '2020/04/31',
        '2020/03-15',
        '-2020-04-19',
        '-2023/05/24',
        'abc-2023/05/24',
        '2024',
        '2024-',
        '2024-05',
        '2024-05-',
        '2024-05-01-',
        '2024-05-01-abc',
        '2024/',
        '2024/05',
        '2024/05/',
        '2024/05/01/',
        '2024/05/01/abc',
        '2024 05 01 abc',
      ],
    });
    test({
      validator: 'isDate',
      args: [{ format: 'MM.DD.YYYY', delimiters: ['.'], strictMode: true }],
      valid: [
        '01.15.2020',
        '02.15.2014',
        '03.15.2014',
        '02.29.2020',
      ],
      invalid: [
        '2014-02-15',
        '2020-02-29',
        '15-07/2002',
        new Date(),
        new Date([2014, 2, 15]),
        new Date('2014-03-15'),
        '29.02.2020',
        '02.29.2020.20',
        '2024-',
        '2024-05',
        '2024-05-',
        '2024-05-01',
        '-2020-04-19',
        '-2023/05/24',
        'abc-2023/05/24',
        '04.05.2024.',
        '04.05.2024.abc',
        'abc.04.05.2024',
      ],
    });
    // emulating Pacific time zone offset & time
    // which could potentially result in UTC conversion issues
    timezone_mock.register('US/Pacific');
    test({
      validator: 'isDate',
      valid: [
        new Date(2016, 2, 29),
        '2017-08-04',
      ],
    });
    timezone_mock.unregister();
  });
});
